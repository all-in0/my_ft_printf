int		ft_atoi(char *str)
{
	int		i;
	int		a;
	int		x;

	x = 0;
	a = 0;
	i = 0;
	while (str[i] <= 32 && str[i] != '\200')
		i++;
	if (str[i] == '-')
		a = 1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] <= '9' && str[i] >= '0' && str[i])
	{
		x = x * 10;
		x += str[i] - '0';
		i++;
	}
	if (a == 1)
		return (-x);
	return (x);
}
